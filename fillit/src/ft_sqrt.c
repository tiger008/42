#include "libft.h"

int	ft_sqrt(int n)
{
	float	sq;
	float	tmp;

	tmp = 1.0;
	sq = 0.5 * (1 + n);
	while (ABS(sq - tmp) > 0.001)
	{
		tmp = sq;
		sq = (0.5 * (tmp + n / tmp));
	}
	return ((int)sq);
}
