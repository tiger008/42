#include "libft.h"

int	ft_sqrt(int n)
{
	int	sq;
	int	tmp;
	int	y;

	y = 0;
	tmp = 1;
	sq = -1;
	while (y <= n)
	{
		y += tmp;
		tmp += 2;
		++n;
	}
	return (sq);
}
